# string library myfunction
# isupper() - this is used for returning "true" if all characters in a string is uppercase
# islower() - this is used for returning "true" if all characters in a string is lowercase
# lower() - for converting uppercase to lowercase
# upper() - for converting lowercase to uppercase

s = 'IDK'
print(s.isupper())
r = 'idk'
print(r.isupper())
q = 'Idk'
print(q.isupper())


print("You're now seeing the lower() & upper() function results")
print(s.lower())
print(r.upper())
